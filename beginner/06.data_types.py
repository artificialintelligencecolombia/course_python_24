# We can index a string's instance variable as well as the string itself
# The index count starts from 0
a = "Daniel maldonado arias"
print(a[3]) # i
print("Dani"[2]) # n 

# We can index as well from last character to start, by using NEGATIVE indexing
print("Dani"[-1]) # i

# We can make numbers human readable by using _ in the 000 spaces

print(5_000_000)